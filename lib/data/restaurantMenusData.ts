/**
 * Returns authentic, category-matched menu items for any given restaurant ID, category name, or restaurant name.
 */
export function getMenuForRestaurant(
  restaurantId: string | number,
  categoryName?: string,
  restaurantName?: string
): RestaurantMenuItem[] {
  const strId = String(restaurantId || "");
  const cat = categoryName || "";
  const name = restaurantName || "";

  // 1. Direct match by Category key
  let menuKey = Object.keys(CATEGORY_SPECIFIC_MENUS).find(
    (k) => k.toLowerCase() === cat.toLowerCase()
  );

  // 2. Match by restaurant ID keywords
  if (!menuKey) {
    if (strId.includes("pizza") || strId === "rest-pizza" || strId === "2") menuKey = "Pizza";
    else if (strId.includes("cold-drinks") || strId === "rest-cold-drinks" || strId === "1") menuKey = "Cold Drinks";
    else if (strId.includes("burger") || strId === "rest-burger" || strId === "3") menuKey = "Burger";
    else if (strId.includes("biryani") || strId === "rest-biryani" || strId === "4") menuKey = "Biryani";
    else if (strId.includes("south-indian") || strId === "rest-south-indian" || strId === "5") menuKey = "South Indian";
    else if (strId.includes("chinese") || strId === "rest-chinese" || strId === "6") menuKey = "Chinese";
    else if (strId.includes("momos") || strId === "rest-momos" || strId === "7") menuKey = "Momos";
    else if (strId.includes("rolls") || strId === "rest-rolls" || strId === "8") menuKey = "Rolls";
    else if (strId.includes("sandwich") || strId === "rest-sandwich" || strId === "9") menuKey = "Sandwich";
    else if (strId.includes("icecream") || strId === "rest-icecream" || strId === "10") menuKey = "Ice Cream";
    else if (strId.includes("cakes") || strId === "rest-cakes" || strId === "11") menuKey = "Cakes";
    else if (strId.includes("coffee") || strId === "rest-coffee" || strId === "12") menuKey = "Coffee";
    else if (strId.includes("tea") || strId === "rest-tea" || strId === "13") menuKey = "Tea";
    else if (strId.includes("juice") || strId === "rest-juice" || strId === "14") menuKey = "Juice";
    else if (strId.includes("healthy") || strId === "rest-healthy" || strId === "15") menuKey = "Healthy Food";
    else if (strId.includes("thali") || strId === "rest-thali" || strId === "16") menuKey = "Thali";
    else if (strId.includes("seafood") || strId === "rest-seafood" || strId === "17") menuKey = "Seafood";
    else if (strId.includes("bbq") || strId === "rest-bbq" || strId === "18") menuKey = "BBQ";
    else if (strId.includes("pasta") || strId === "rest-pasta" || strId === "19") menuKey = "Pasta";
    else if (strId.includes("shawarma") || strId === "rest-shawarma" || strId === "20") menuKey = "Shawarma";
    else if (strId.includes("tandoori") || strId === "rest-tandoori" || strId === "21") menuKey = "Tandoori";
    else if (strId.includes("north-indian") || strId === "rest-north-indian" || strId === "22") menuKey = "North Indian";
    else if (strId.includes("street-food") || strId === "rest-street-food" || strId === "23") menuKey = "Street Food";
    else if (strId.includes("bakery") || strId === "rest-bakery" || strId === "24") menuKey = "Bakery";
    else if (strId.includes("fast-food") || strId === "rest-fast-food" || strId === "25") menuKey = "Fast Food";
    else if (strId.includes("desserts") || strId === "rest-desserts" || strId === "26") menuKey = "Desserts";
    else if (strId.includes("salads") || strId === "rest-salads" || strId === "27") menuKey = "Salads";
    else if (strId.includes("smoothies") || strId === "rest-smoothies" || strId === "28") menuKey = "Smoothies";
    else if (strId.includes("milkshakes") || strId === "rest-milkshakes" || strId === "29") menuKey = "Milkshakes";
    else if (strId.includes("snacks") || strId === "rest-snacks" || strId === "30") menuKey = "Snacks";
  }

  // 3. Match by restaurant name keywords
  if (!menuKey) {
    const lowerName = name.toLowerCase();
    if (lowerName.includes("pizza")) menuKey = "Pizza";
    else if (lowerName.includes("soda") || lowerName.includes("drink") || lowerName.includes("beverage")) menuKey = "Cold Drinks";
    else if (lowerName.includes("burger")) menuKey = "Burger";
    else if (lowerName.includes("biryani")) menuKey = "Biryani";
    else if (lowerName.includes("dosa") || lowerName.includes("south")) menuKey = "South Indian";
    else if (lowerName.includes("chinese") || lowerName.includes("wok")) menuKey = "Chinese";
    else if (lowerName.includes("momo")) menuKey = "Momos";
    else if (lowerName.includes("roll")) menuKey = "Rolls";
    else if (lowerName.includes("sandwich") || lowerName.includes("toast")) menuKey = "Sandwich";
    else if (lowerName.includes("ice cream") || lowerName.includes("scoop")) menuKey = "Ice Cream";
    else if (lowerName.includes("cake") || lowerName.includes("patisserie")) menuKey = "Cakes";
    else if (lowerName.includes("coffee") || lowerName.includes("espresso")) menuKey = "Coffee";
    else if (lowerName.includes("chai") || lowerName.includes("tea")) menuKey = "Tea";
    else if (lowerName.includes("juice") || lowerName.includes("squeeze")) menuKey = "Juice";
    else if (lowerName.includes("green") || lowerName.includes("healthy") || lowerName.includes("clean")) menuKey = "Healthy Food";
    else if (lowerName.includes("thali")) menuKey = "Thali";
    else if (lowerName.includes("seafood") || lowerName.includes("catch")) menuKey = "Seafood";
    else if (lowerName.includes("bbq") || lowerName.includes("smoke") || lowerName.includes("grill")) menuKey = "BBQ";
    else if (lowerName.includes("pasta") || lowerName.includes("trattoria")) menuKey = "Pasta";
    else if (lowerName.includes("shawarma")) menuKey = "Shawarma";
    else if (lowerName.includes("tandoori") || lowerName.includes("kebab")) menuKey = "Tandoori";
    else if (lowerName.includes("punjab") || lowerName.includes("curry") || lowerName.includes("north")) menuKey = "North Indian";
    else if (lowerName.includes("street") || lowerName.includes("chaat") || lowerName.includes("bazaar")) menuKey = "Street Food";
    else if (lowerName.includes("bakery") || lowerName.includes("artisan")) menuKey = "Bakery";
    else if (lowerName.includes("quick") || lowerName.includes("fast")) menuKey = "Fast Food";
    else if (lowerName.includes("sweet") || lowerName.includes("waffle")) menuKey = "Desserts";
    else if (lowerName.includes("salad") || lowerName.includes("crunch")) menuKey = "Salads";
    else if (lowerName.includes("smoothie")) menuKey = "Smoothies";
    else if (lowerName.includes("shake") || lowerName.includes("thickshake")) menuKey = "Milkshakes";
    else if (lowerName.includes("snack") || lowerName.includes("munchies")) menuKey = "Snacks";
  }

  // Fallback to Pizza if unmatched
  const baseItems = CATEGORY_SPECIFIC_MENUS[menuKey || "Pizza"] || CATEGORY_SPECIFIC_MENUS["Pizza"];

  // Attach restaurantId to each menu item
  return baseItems.map((item, idx) => ({
    ...item,
    id: `${strId || 'rest'}-${item.id || idx}`,
    restaurantId: strId,
    restaurant_id: strId,
  }));
}
